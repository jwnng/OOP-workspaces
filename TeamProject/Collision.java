    // 캐릭터(사각형)와 벽이 닿았는지 확인하는 메서드
    public static boolean isColliding(int x, int y, int w, int h) {
        return check(x, y) || check(x + w, y) || check(x, y + h) || check(x + w, y + h);
    }

    private static boolean check(int x, int y) {
        int tx = x / TILE_SIZE;
        int ty = y / TILE_SIZE;
        
        // 맵 범위 밖 체크
        if (ty < 0 || ty >= tileMap.length || tx < 0 || tx >= tileMap[0].length) return true;
        
        return tileMap[ty][tx] == 1; // 1이면 벽!
    }
